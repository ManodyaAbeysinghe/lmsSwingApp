
package com.industrialmaster.lms.model;


public class Category {
  private int id;

private String category;
private String category_persian;

    
    public int getId() {
        return id;
    }

   
    public void setId(int id) {
        this.id = id;
    }

    
    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getCategory_persian() {
        return category_persian;
    }

    public void setCategory_persian(String category_persian) {
        this.category_persian = category_persian;
    }

   



}
