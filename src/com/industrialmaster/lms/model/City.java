
package com.industrialmaster.lms.model;


public class City {
  private int id;

private String city;
private String city_persian;

    
    public int getId() {
        return id;
    }

    
    public void setId(int id) {
        this.id = id;
    }

    
    public String getCity() {
        return city;
    }

    
    public void setCity(String city) {
        this.city = city;
    }

   
    public String getCity_persian() {
        return city_persian;
    }

   
    public void setCity_persian(String city_persian) {
        this.city_persian = city_persian;
    }



}
