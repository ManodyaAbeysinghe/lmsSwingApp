
package com.industrialmaster.lms.model;


public class Gender {
    private int id;
    
    private String gender;
    private String gender_persian;

    
    public int getId() {
        return id;
    }

    
    public void setId(int id) {
        this.id = id;
    }

    
    public String getGender() {
        return gender;
    }

    
    public void setGender(String gender) {
        this.gender = gender;
    }

    
    public String getGender_persian() {
        return gender_persian;
    }

    
    public void setGender_persian(String gender_persian) {
        this.gender_persian = gender_persian;
    }
    
    
    
}
