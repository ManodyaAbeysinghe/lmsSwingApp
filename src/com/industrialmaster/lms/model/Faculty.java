
package com.industrialmaster.lms.model;


public class Faculty {
    private int id;
    
    private String faculty;
    private String faculty_persian;

   
    public int getId() {
        return id;
    }

    
    public void setId(int id) {
        this.id = id;
    }

    
    public String getFaculty() {
        return faculty;
    }

    
    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    
    public String getFaculty_persian() {
        return faculty_persian;
    }

   
    public void setFaculty_persian(String faculty_persian) {
        this.faculty_persian = faculty_persian;
    }
    
    
    
    
}
