    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String user_id=txtUser_id.getText();
        String copy_id=txtCopy_id.getText();
       String deposit_date = txtDeposits_date.getText();
       String due_date = txtDue_date.getText();
       
       
       
       //2.Process data before store
       //a. Validations

       
       
       //b. Generate Data (Non fillables)
      
       
       Deposits deposit = new Deposits();
        deposit.setUser_id(user_id);
       deposit.setCopy_id(copy_id);
       deposit.setDeposit_date(deposit_date);
        deposit.setDue_date(due_date);
       
      
       
      boolean done = DepositsController.save(deposit);
       if(done){
       JOptionPane.showMessageDialog(this,"Saved");
       }else{
       JOptionPane.showMessageDialog(this,"Error");
       }
       
       loadToTable();
        
    }//GEN-LAST:event_btnSaveActionPerformed

