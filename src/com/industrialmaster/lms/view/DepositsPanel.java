    private void tblDepositsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDepositsMouseClicked
        // TODO add your handling code here:
           int row = tblDeposits.getSelectedRow();
       if(row!=-1){//if the row selected
       
           String idstr= tblDeposits.getValueAt(row, 0).toString();
           id = Integer.parseInt(idstr);
           Deposits deposits = DepositsController.getOne(id);
           txtUser_id.setText(deposits.getUser_id()+"");
           txtCopy_id.setText(deposits.getCopy_id());
           txtDeposits_date.setText(deposits.getDeposit_date()+"");
           txtDue_date.setText(deposits.getDue_date()+"");
         
       }
        
    }//GEN-LAST:event_tblDepositsMouseClicked

